//JavaScript Arithmetic Operators

var x = 50, y = 10;

console.log( 'x + y ', x + y );
console.log( 'x + y ', x - y );
console.log( 'x + y ', x * y );
console.log( 'x + y ', x / y );
console.log( 'x + y ', x % y );

console.log( 'x power 2', x ** 2);

console.log( 'x++ ', x++ );
console.log( 'x-- ', x-- );

console.log( '++x ', ++x);
console.log( '--x ', --x);